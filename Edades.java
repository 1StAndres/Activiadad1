
public class Edades {
    public static void main(String[] args){
        int juan=9;
        int albert,ana,madre;
        albert=(juan/3)*2;
        ana=(juan/3)*4;
        madre=juan+albert+ana;
        System.out.println("Las edades son:");
        System.out.println("Alberto: "+albert);
        System.out.println("Juan: "+juan);
        System.out.println("Ana: "+ana);
        System.out.println("Madre: "+madre);
    }
}
